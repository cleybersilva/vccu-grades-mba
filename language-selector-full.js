// Current language
let currentLang = localStorage.getItem('vccu-lang') || 'en';

// Apply translations to all panels
function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;
  
  currentLang = lang;
  localStorage.setItem('vccu-lang', lang);
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;
  
  // Update selector button
  const btn = document.getElementById('lang-btn');
  if (btn) btn.innerHTML = t.flag + ' <span class="lang-arrow">▼</span>';
  
  // Mark active option
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.textContent.includes(t.name));
  });
  
  // ===== HEADER =====
  const hlbl = document.querySelector('.hlbl');
  if (hlbl) hlbl.textContent = t.headerInstitution;
  
  const hh1 = document.querySelector('.hh1');
  if (hh1) hh1.innerHTML = t.headerTitle;
  
  const aiItem = document.querySelector('.ai-item');
  if (aiItem) aiItem.textContent = t.mbaAI;
  
  const dsoItem = document.querySelector('.dso-item');
  if (dsoItem) dsoItem.textContent = t.mbaDSO;
  
  const hsub = document.querySelector('.hsub');
  if (hsub) hsub.innerHTML = t.headerSub;
  
  const onlineBadge = document.querySelector('.online-badge');
  if (onlineBadge) onlineBadge.innerHTML = '<span class="online-dot"></span> ' + t.onlineBadge;
  
  // ===== TABS =====
  const tabs = document.querySelectorAll('.tb button');
  const tabTexts = [t.tabOverview, t.tabAI, t.tabDSO, t.tabEval, t.tabFaculty, t.tabRefs, t.tabEmentaAI, t.tabEmentaDSO];
  tabs.forEach((tab, i) => {
    if (tabTexts[i]) tab.textContent = tabTexts[i];
  });
  
  // ===== PANEL 0 - OVERVIEW =====
  const panel0 = document.getElementById('panel-0');
  if (panel0) {
    // Cards
    const cards = panel0.querySelectorAll('.card');
    if (cards.length >= 4) {
      const ct1 = cards[0].querySelector('.ct');
      const cl1 = cards[0].querySelector('.cl');
      const cv1 = cards[0].querySelector('.cv');
      if (ct1) ct1.textContent = t.cardAITitle;
      if (cl1) cl1.textContent = t.cardDuration;
      if (cv1) cv1.textContent = t.cardDisciplines;
      
      const ct2 = cards[1].querySelector('.ct');
      const cl2 = cards[1].querySelector('.cl');
      const cv2 = cards[1].querySelector('.cv');
      if (ct2) ct2.textContent = t.cardDSOTitle;
      if (cl2) cl2.textContent = t.cardDuration;
      if (cv2) cv2.textContent = t.cardDisciplines;
      
      const ct3 = cards[2].querySelector('.ct');
      const cl3 = cards[2].querySelector('.cl');
      const cv3 = cards[2].querySelector('.cv');
      if (ct3) ct3.textContent = t.cardTotal;
      if (cl3) cl3.textContent = t.cardTotalVal;
      if (cv3) cv3.textContent = t.cardTotalDesc;
      
      const ct4 = cards[3].querySelector('.ct');
      const cl4 = cards[3].querySelector('.cl');
      const cv4 = cards[3].querySelector('.cv');
      if (ct4) ct4.textContent = t.cardRefs;
      if (cl4) cl4.textContent = t.cardRefsVal;
      if (cv4) cv4.textContent = t.cardRefsDesc;
    }
    
    // Section titles
    const stitle = panel0.querySelectorAll('.stitle');
    if (stitle.length >= 2) {
      stitle[0].textContent = t.sectionStructure;
      stitle[1].textContent = t.sectionRefs;
    }
    
    // Program cards
    const phm = panel0.querySelectorAll('.phm');
    phm.forEach(el => el.textContent = t.programType);
    
    const pts = panel0.querySelectorAll('.pt');
    if (pts.length >= 2) {
      pts[0].textContent = t.programAITitle;
      pts[1].textContent = t.programDSOTitle;
    }
    
    const pds = panel0.querySelectorAll('.pd');
    if (pds.length >= 2) {
      pds[0].textContent = t.programAIDesc;
      pds[1].textContent = t.programDSODesc;
    }
    
    // Semester pills
    const pms = panel0.querySelectorAll('.pm');
    const pmTexts = [t.sem1AI, t.sem2AI, t.sem3AI, t.online100, t.sem1DSO, t.sem2DSO, t.sem3DSO, t.online100];
    let pmIndex = 0;
    pms.forEach(pm => {
      if (pm.classList.contains('ai') || pm.classList.contains('dso') || pm.classList.contains('online')) {
        if (pmTexts[pmIndex]) pm.textContent = pmTexts[pmIndex];
        pmIndex++;
      }
    });
  }
  
  // ===== PANEL 1 - AI PROGRAM =====
  const panel1 = document.getElementById('panel-1');
  if (panel1) {
    const progHdr = panel1.querySelector('.prog-hdr');
    if (progHdr) {
      const progEy = progHdr.querySelector('.prog-ey');
      const progTi = progHdr.querySelector('.prog-ti');
      const progTg = progHdr.querySelector('.prog-tg');
      if (progEy) progEy.textContent = t.programType;
      if (progTi) progTi.textContent = t.programAITitle;
      if (progTg) progTg.textContent = t.aiProgramDesc;
    }
  }
  
  // ===== PANEL 2 - DSO PROGRAM =====
  const panel2 = document.getElementById('panel-2');
  if (panel2) {
    const progHdr = panel2.querySelector('.prog-hdr');
    if (progHdr) {
      const progEy = progHdr.querySelector('.prog-ey');
      const progTi = progHdr.querySelector('.prog-ti');
      const progTg = progHdr.querySelector('.prog-tg');
      if (progEy) progEy.textContent = t.programType;
      if (progTi) progTi.textContent = t.programDSOTitle;
      if (progTg) progTg.textContent = t.dsoProgramDesc;
    }
  }
  
  // ===== PANEL 3 - EVALUATION =====
  const panel3 = document.getElementById('panel-3');
  if (panel3) {
    const ecs = panel3.querySelectorAll('.ec');
    const evalData = [
      { title: t.evalContinuous, desc: t.evalContinuousDesc },
      { title: t.evalPractical, desc: t.evalPracticalDesc },
      { title: t.evalFinal, desc: t.evalFinalDesc }
    ];
    ecs.forEach((ec, i) => {
      if (evalData[i]) {
        const et = ec.querySelector('.et');
        const ed = ec.querySelector('.ed');
        if (et) et.textContent = evalData[i].title;
        if (ed) ed.textContent = evalData[i].desc;
      }
    });
  }
  
  // ===== PANEL 4 - FACULTY =====
  const panel4 = document.getElementById('panel-4');
  if (panel4) {
    const stitle = panel4.querySelector('.stitle');
    if (stitle) stitle.textContent = t.facultyTitle;
  }
  
  // ===== PANEL 5 - REFERENCES =====
  const panel5 = document.getElementById('panel-5');
  if (panel5) {
    const stitle = panel5.querySelector('.stitle');
    if (stitle) stitle.textContent = t.refsTitle;
  }
  
  // ===== PANEL 6 & 7 - EMENTAS =====
  const panel6 = document.getElementById('panel-6');
  if (panel6) {
    const banner = panel6.querySelector('.ementa-banner');
    if (banner) {
      const ebLabel = banner.querySelector('.eb-label');
      const ebTitle = banner.querySelector('.eb-title');
      if (ebLabel) ebLabel.textContent = t.programType;
      if (ebTitle) ebTitle.textContent = t.programAITitle;
    }
  }
  
  const panel7 = document.getElementById('panel-7');
  if (panel7) {
    const banner = panel7.querySelector('.ementa-banner');
    if (banner) {
      const ebLabel = banner.querySelector('.eb-label');
      const ebTitle = banner.querySelector('.eb-title');
      if (ebLabel) ebLabel.textContent = t.programType;
      if (ebTitle) ebTitle.textContent = t.programDSOTitle;
    }
  }
  
  // ===== FOOTER =====
  const fcopy = document.querySelector('.fcopy');
  if (fcopy) {
    fcopy.innerHTML = '© 2025/2026 Veni Creator Christian University (VCCU) — ' + t.footerRights + '<br>' + t.footerDisclaimer;
  }
  
  const flinks = document.querySelector('.flinks');
  if (flinks && t.footerLinks) {
    const spans = flinks.querySelectorAll('span');
    t.footerLinks.forEach((text, i) => {
      if (spans[i]) spans[i].textContent = text;
    });
  }
  
  // Close dropdown
  const langDropdown = document.getElementById('lang-dropdown');
  if (langDropdown) langDropdown.classList.remove('open');
}

// Toggle dropdown
function toggleLangDropdown(e) {
  e.stopPropagation();
  const dropdown = document.getElementById('lang-dropdown');
  dropdown.classList.toggle('open');
}


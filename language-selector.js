// Current language
let currentLang = localStorage.getItem('vccu-lang') || 'en';

// Apply translations
function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;
  
  currentLang = lang;
  localStorage.setItem('vccu-lang', lang);
  
  // Update selector button
  const btn = document.getElementById('lang-btn');
  if (btn) btn.innerHTML = t.flag + ' <span class="lang-arrow">▼</span>';
  
  // Header
  const hlbl = document.querySelector('.hlbl');
  if (hlbl) hlbl.textContent = t.headerInstitution;
  
  const hh1 = document.querySelector('.hh1');
  if (hh1) hh1.innerHTML = t.headerTitle;
  
  const aiItem = document.querySelector('.ai-item');
  if (aiItem) aiItem.textContent = t.mbaAI;
  
  const dsoItem = document.querySelector('.dso-item');
  if (dsoItem) dsoItem.textContent = t.mbaDSO;
  
  const hsub = document.querySelector('.hsub');
  if (hsub) hsub.innerHTML = t.headerSub;
  
  const onlineBadge = document.querySelector('.online-badge');
  if (onlineBadge) onlineBadge.innerHTML = '<span class="online-dot"></span> ' + t.onlineBadge;
  
  // Tabs
  const tabs = document.querySelectorAll('.tb button');
  const tabTexts = [t.tabOverview, t.tabAI, t.tabDSO, t.tabEval, t.tabFaculty, t.tabRefs, t.tabEmentaAI, t.tabEmentaDSO];
  tabs.forEach((tab, i) => {
    if (tabTexts[i]) tab.textContent = tabTexts[i];
  });
  
  // Cards in overview
  const cards = document.querySelectorAll('#panel-0 .card');
  if (cards.length >= 4) {
    // Card 1 - AI
    const ct1 = cards[0].querySelector('.ct');
    const cl1 = cards[0].querySelector('.cl');
    const cv1 = cards[0].querySelector('.cv');
    if (ct1) ct1.textContent = t.cardAI;
    if (cl1) cl1.textContent = t.cardDuration;
    if (cv1) cv1.textContent = t.cardDisciplines;
    
    // Card 2 - DSO
    const ct2 = cards[1].querySelector('.ct');
    const cl2 = cards[1].querySelector('.cl');
    const cv2 = cards[1].querySelector('.cv');
    if (ct2) ct2.textContent = t.cardDSO;
    if (cl2) cl2.textContent = t.cardDuration;
    if (cv2) cv2.textContent = t.cardDisciplines;
    
    // Card 3 - Total
    const ct3 = cards[2].querySelector('.ct');
    const cl3 = cards[2].querySelector('.cl');
    const cv3 = cards[2].querySelector('.cv');
    if (ct3) ct3.textContent = t.cardTotal;
    if (cl3) cl3.textContent = t.cardTotalVal;
    if (cv3) cv3.textContent = t.cardTotalDesc;
    
    // Card 4 - Refs
    const ct4 = cards[3].querySelector('.ct');
    const cl4 = cards[3].querySelector('.cl');
    const cv4 = cards[3].querySelector('.cv');
    if (ct4) ct4.textContent = t.cardRefs;
    if (cl4) cl4.textContent = t.cardRefsVal;
    if (cv4) cv4.textContent = t.cardRefsDesc;
  }
  
  // Section titles
  const stitle = document.querySelectorAll('#panel-0 .stitle');
  if (stitle.length >= 2) {
    stitle[0].textContent = t.sectionStructure;
    stitle[1].textContent = t.sectionRefs;
  }
  
  // Program descriptions
  const pds = document.querySelectorAll('#panel-0 .pd');
  if (pds.length >= 2) {
    pds[0].textContent = t.programAI;
    pds[1].textContent = t.programDSO;
  }
  
  // Footer
  const fcopy = document.querySelector('.fcopy');
  if (fcopy) {
    fcopy.innerHTML = '© 2025/2026 Veni Creator Christian University (VCCU) — ' + t.footerRights + '<br>' + t.footerDisclaimer;
  }
  
  // Close dropdown
  const langDropdown = document.getElementById('lang-dropdown');
  if (langDropdown) langDropdown.classList.remove('open');
}

// Toggle dropdown
function toggleLangDropdown(e) {
  e.stopPropagation();
  const dropdown = document.getElementById('lang-dropdown');
  dropdown.classList.toggle('open');
}

// Select language
function selectLang(lang) {
  applyTranslations(lang);
}

// Close dropdown on outside click
document.addEventListener('click', function() {
  const dropdown = document.getElementById('lang-dropdown');
  if (dropdown) dropdown.classList.remove('open');
});

// Initialize on load
document.addEventListener('DOMContentLoaded', function() {
  applyTranslations(currentLang);
});
